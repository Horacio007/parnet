            <center><h1 style="color: #fffb00"><strong>Data Centers</strong></h1></center><br>
            <p style="color: white">
            Un data center es un centro de procesamiento de datos, una instalación empleada para albergar un sistema de información de componentes asociados,
            como telecomunicaciones y los sistemas de almacenamientos donde generalmente incluyen fuentes de alimentación redundante o de respaldo de un proyecto
            típico de data center que ofrece espacio para hardware en un ambiente controlado, como por ejemplo acondicionando el espacio con el aire acondicionado,
            extinción de encendidos de diferentes dispositivos de seguridad para permitir que los equipos tengan el mejor nivel de rendimiento con la máxima disponibilidad
            del sistema.
            </p>
            <br><br>
            <center><h1 style="color: #fffb00"><strong>Tipos de Data Centers</strong></h1></center><br>
            <ul style="color: white">
                <li>
                    Tier I. Se trata del tipo más básico de centro de datos. Sólo pueden garantizar la continuidad del servicio al 99,671%. Esto, aunque parezca una gran
                    cantidad, en realidad es la más baja que existe. En un data center de Tier 1 se puede interrumpir el servicio sin previo aviso. No cuentan, además, con
                    sistemas de redundancia y refrigeración.
                </li>
                <br>
                <li>
                    Tier II. Su disponibilidad es algo superior a la del anterior, del 99,741%. Disponen de sistemas de redundancia, generadores auxiliares de energía
                    y suelos elevados, entre otras ventajas. Sin embargo, aún pueden presentar interrupciones imprevistas.
                </li>
                <br>
                <li>
                    Tier III. Los data center del tipo Tier III no se ven afectados por interrupciones no programadas. Es decir, sólo cuando se van a llevar a cabo acciones
                    de mantenimiento, los servidores afectados de dicho tipo de data center interrumpirán su servicio. Están conectados a diferentes redes eléctricas y cuentan
                    con un sistema de refrigeración avanzado. Su disponibilidad es del 99,98%.
                </li>
                <br>
                <li>
                    Tier IV. Los Tier IV son el tipo más avanzado de centro de datos y ofrecen una disponibilidad de 99,995%. Esto significa que sólo se detienen durante
                    26 minutos a lo largo de todo año. Se trata del nivel más alto de disponibilidad. Cuentan con un alto nivel de redundancia, sistemas de refrigeración
                    de alta eficiencia. Disponen también de redes de fibra complementaria y sistemas de suministro eléctrico alternativo.
                </li>
            </ul>
